#!/usr/bin/env python3
# -*- coding: utf-8 -*-
# usage: python collect_qpadm_results.py --strategy rotating --source-file source.txt --target-file target.txt --right-file right.txt --out qpadm_summary.xlsx --tail 0.05
"""
Collect qpAdm batch result.txt files into an Excel workbook.

Designed for the directory layout generated by qpadm_v4.sh:
  output/<target>/<n-way>/<source1-source2-...>/result.txt

Usage:
  python collect_qpadm_results.py --out qpadm_summary.xlsx --tail 0.05
  python collect_qpadm_results.py --root output --target-file target.txt --source-file source.txt --right-file right.txt

Successful model criteria:
  1. all best coefficients > 0
  2. every std. error < corresponding best coefficient
  3. tail value for f4rank = n_way - 1 > threshold
"""

from __future__ import annotations

import argparse
import re
from pathlib import Path
from typing import Iterable

try:
    import pandas as pd
    from openpyxl import Workbook
    from openpyxl.styles import Alignment, Font, PatternFill, Border, Side
    from openpyxl.utils import get_column_letter
    from openpyxl.utils.dataframe import dataframe_to_rows
except ImportError as exc:
    raise SystemExit("Missing dependency: pandas/openpyxl. Install with: pip install pandas openpyxl") from exc


NUMBER_RE = re.compile(r"[-+]?(?:\d+(?:\.\d*)?|\.\d+)(?:[eE][-+]?\d+)?")
CJK_RE = re.compile(r"[\u3400-\u9fff]")
LATIN_FONT = "Times New Roman"
CJK_FONT = "SimSun"


def read_lines(path: Path | None) -> list[str]:
    if not path:
        return []
    if not path.exists():
        return []
    return [line.strip() for line in path.read_text(encoding="utf-8", errors="ignore").splitlines() if line.strip()]


def parse_source_file(path: Path | None) -> list[str]:
    if not path or not path.exists():
        return []
    out: list[str] = []
    for line in path.read_text(encoding="utf-8", errors="ignore").splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "====" in line:
            continue
        out.append(line)
    return out


def parse_source_groups(path: Path | None) -> list[tuple[str | None, list[str]]]:
    if not path or not path.exists():
        return []
    groups: list[tuple[str | None, list[str]]] = []
    current_name: str | None = None
    current_pops: list[str] = []
    for raw in path.read_text(encoding="utf-8", errors="ignore").splitlines():
        line = raw.strip()
        if not line:
            continue
        tag_match = re.fullmatch(r"=+\s*(.*?)\s*=+", line)
        if tag_match:
            if current_name is not None or current_pops:
                groups.append((current_name, current_pops))
            current_name = tag_match.group(1)
            current_pops = []
            continue
        if line.startswith("#"):
            continue
        current_pops.append(line)
    if current_name is not None or current_pops:
        groups.append((current_name, current_pops))
    return groups


def numbers_from_text(text: str) -> list[float]:
    vals: list[float] = []
    for token in NUMBER_RE.findall(text):
        try:
            vals.append(float(token))
        except ValueError:
            pass
    return vals


def collect_numbers_near_label(lines: list[str], label_regex: str, expected: int) -> list[float]:
    pat = re.compile(label_regex, re.IGNORECASE)
    for i, line in enumerate(lines):
        if not pat.search(line):
            continue
        tail = line.split(":", 1)[1] if ":" in line else line
        vals = numbers_from_text(tail)
        j = i + 1
        while len(vals) < expected and j < len(lines):
            nxt = lines[j].strip()
            if not nxt or re.search(r"^(summ|fixed|best|std|jackknife|f4rank|coeff|left)", nxt, re.IGNORECASE):
                break
            vals.extend(numbers_from_text(nxt))
            j += 1
        if vals:
            return vals[:expected]
    return []


def parse_tail(lines: list[str], rank: int) -> float | None:
    f4rank_re = re.compile(rf"\bf4rank:\s*{rank}\b", re.IGNORECASE)
    for line in lines:
        if not f4rank_re.search(line):
            continue
        tail_match = re.search(r"\btail:\s*(" + NUMBER_RE.pattern + r")", line, re.IGNORECASE)
        if tail_match:
            return float(tail_match.group(1))
        fields = line.split()
        if len(fields) >= 8:
            try:
                return float(fields[7])
            except ValueError:
                pass
        vals = numbers_from_text(line)
        if vals:
            return vals[-1]
    return None


def infer_from_path(result_path: Path) -> tuple[str, int | None, list[str]]:
    combo_dir = result_path.parent
    way_dir = combo_dir.parent
    target_dir = way_dir.parent

    target = target_dir.name
    way_match = re.search(r"(\d+)\s*-\s*way", way_dir.name, re.IGNORECASE)
    n_way = int(way_match.group(1)) if way_match else None
    sources = combo_dir.name.split("-") if combo_dir.name else []
    return target, n_way, sources


def read_model_pops(result_path: Path) -> tuple[str, list[str], list[str]]:
    left = read_lines(result_path.parent / "left.pops")
    right = read_lines(result_path.parent / "right.pops")
    if left:
        return left[0], left[1:], right
    target, _n_way, sources = infer_from_path(result_path)
    return target, sources, right


def parse_result(result_path: Path, tail_threshold: float) -> dict:
    text = result_path.read_text(encoding="utf-8", errors="ignore")
    lines = text.splitlines()
    target, path_n_way, path_sources = infer_from_path(result_path)
    pop_target, sources, outgroups = read_model_pops(result_path)
    target = pop_target or target
    if not sources:
        sources = path_sources
    n_way = len(sources) or path_n_way or 0

    coeffs = collect_numbers_near_label(lines, r"\bbest\s+coefficients?\b", n_way)
    errors = collect_numbers_near_label(lines, r"\bstd\.?\s*errors?\b", n_way)
    jackknife = collect_numbers_near_label(lines, r"\bjackknife\b.*\b(mean|estimate|coeff|coefficients?)\b|\bjackknife\b", n_way)
    tail = parse_tail(lines, max(n_way - 1, 0))

    reasons: list[str] = []
    if len(coeffs) != n_way:
        reasons.append("missing best coefficient")
    elif any(v <= 0 for v in coeffs):
        reasons.append("coefficient <= 0")

    if len(errors) != n_way:
        reasons.append("missing std. error")
    elif len(coeffs) == n_way and any(se >= coef for se, coef in zip(errors, coeffs)):
        reasons.append("std.error >= coefficient")

    if tail is None:
        reasons.append("missing tail")
    elif tail <= tail_threshold:
        reasons.append(f"tail <= {tail_threshold:g}")

    row = {
        "target": target,
        "n_way": n_way,
        "sources": ";".join(sources),
        "outgroups": ";".join(outgroups),
        "tail": tail,
        "tail_threshold": tail_threshold,
        "pass": len(reasons) == 0,
        "fail_reason": "; ".join(reasons),
        "result_path": str(result_path),
    }
    for i, src in enumerate(sources, start=1):
        row[f"source{i}"] = src
    for i, val in enumerate(coeffs, start=1):
        row[f"ratio{i}"] = val
    for i, val in enumerate(errors, start=1):
        row[f"std.error{i}"] = val
    for i, val in enumerate(jackknife, start=1):
        row[f"Jackknife{i}"] = val
    return row


def max_index(rows: Iterable[dict], prefix: str) -> int:
    out = 0
    for row in rows:
        for key in row:
            if key.startswith(prefix):
                suffix = key[len(prefix) :]
                if suffix.isdigit():
                    out = max(out, int(suffix))
    return out


def ordered_result_columns(rows: list[dict]) -> list[str]:
    max_sources = max(max_index(rows, "source"), max((r.get("n_way") or 0 for r in rows), default=0))
    cols = ["target"] + [f"source{i}" for i in range(1, max_sources + 1)]
    cols += [f"ratio{i}" for i in range(1, max_sources + 1)]
    cols += [f"std.error{i}" for i in range(1, max_sources + 1)]
    cols += ["tail"] + [f"Jackknife{i}" for i in range(1, max_sources + 1)]
    cols += ["n_way", "strategy", "pass", "fail_reason", "sources", "outgroups", "result_path"]
    return cols


def infer_strategy(rows: list[dict], source_pops: list[str]) -> str | None:
    source_set = set(source_pops)
    if not source_set:
        return None
    saw_right_pops = False
    for row in rows:
        selected = {s for s in str(row.get("sources", "")).split(";") if s}
        rights = {g for g in str(row.get("outgroups", "")).split(";") if g}
        if rights:
            saw_right_pops = True
        if any((pop in source_set and pop not in selected) for pop in rights):
            return "rotating"
    if saw_right_pops:
        return "non-rotating"
    return None


def strategy_source_label(strategy: str | None) -> str | None:
    if not strategy:
        return None
    normalized = strategy.strip().lower()
    if normalized == "rotating":
        return "#rotation"
    if normalized in {"non-rotating", "non_rotating", "nonrotating"}:
        return "#combination"
    return f"#{strategy}"


def build_source_display(source_groups: list[tuple[str | None, list[str]]], fallback_sources: list[str], strategy: str | None) -> list[str]:
    values: list[str] = []
    strategy_label = strategy_source_label(strategy)
    if strategy_label:
        values.append(strategy_label)
    if source_groups:
        for group_idx, (group_name, pops) in enumerate(source_groups):
            if group_idx > 0:
                values.append("")
            if group_name:
                values.append(f"#{group_name}")
            values.extend(pops)
    else:
        values.extend(fallback_sources)
    return values


def font_for_value(value, bold: bool = False, size: int | None = None) -> Font:
    name = CJK_FONT if isinstance(value, str) and CJK_RE.search(value) else LATIN_FONT
    return Font(name=name, bold=bold, color="000000", size=size)


def style_header_row(ws, row_idx: int, min_col: int, max_col: int) -> None:
    fill = PatternFill(fill_type="solid", fgColor="FFFFFF")
    border = Border(bottom=Side(style="thin", color="808080"))
    for col in range(min_col, max_col + 1):
        cell = ws.cell(row_idx, col)
        cell.font = font_for_value(cell.value, bold=True)
        cell.fill = fill
        cell.border = border
        cell.alignment = Alignment(horizontal="center")


def write_dataframe(ws, df: pd.DataFrame, start_row: int) -> int:
    for r_idx, row in enumerate(dataframe_to_rows(df, index=False, header=True), start=start_row):
        for c_idx, value in enumerate(row, start=1):
            ws.cell(r_idx, c_idx, value)
    return start_row + len(df) + 1


def write_excel(
    rows: list[dict],
    out_path: Path,
    targets: list[str],
    sources: list[str],
    source_groups: list[tuple[str | None, list[str]]],
    outgroups: list[str],
    strategy: str | None,
    tail_threshold: float,
) -> None:
    columns = ordered_result_columns(rows)
    all_df = pd.DataFrame(rows)
    for col in columns:
        if col not in all_df.columns:
            all_df[col] = None
    all_df = all_df[columns].sort_values(["target", "n_way", "sources"], na_position="last")
    passed_df = all_df[all_df["pass"] == True].copy()

    source_display = build_source_display(source_groups, sources, strategy)
    input_rows = max(len(targets), len(source_display), len(outgroups))
    out_path.parent.mkdir(parents=True, exist_ok=True)

    wb = Workbook()
    ws = wb.active
    ws.title = "qpAdm"
    ws.sheet_view.showGridLines = False

    ws.append(["Targets", "Sources", "Fixed outgroups"])
    style_header_row(ws, 1, 1, 3)
    for idx in range(input_rows):
        ws.cell(idx + 2, 1, targets[idx] if idx < len(targets) else None)
        ws.cell(idx + 2, 2, source_display[idx] if idx < len(source_display) else None)
        ws.cell(idx + 2, 3, outgroups[idx] if idx < len(outgroups) else None)

    for row_idx in range(2, input_rows + 2):
        val = ws.cell(row_idx, 2).value
        if isinstance(val, str) and val.startswith("#"):
            ws.cell(row_idx, 2).font = font_for_value(val)

    meta_row = input_rows + 3
    summary_items = [
        ("strategy", strategy or ""),
        ("tail_threshold", tail_threshold),
        ("total_models", len(all_df)),
        ("passed_models", len(passed_df)),
        ("failed_models", len(all_df) - len(passed_df)),
    ]
    for offset, (key, value) in enumerate(summary_items):
        ws.cell(meta_row + offset, 1, key)
        ws.cell(meta_row + offset, 2, value)

    passed_title_row = meta_row + len(summary_items) + 2
    ws.cell(passed_title_row, 1, "Passed results")
    ws.cell(passed_title_row, 1).font = font_for_value("Passed results", bold=True, size=12)
    passed_header_row = passed_title_row + 1
    next_row = write_dataframe(ws, passed_df, passed_header_row)
    style_header_row(ws, passed_header_row, 1, len(columns))

    all_title_row = next_row + 2
    ws.cell(all_title_row, 1, "All results")
    ws.cell(all_title_row, 1).font = font_for_value("All results", bold=True, size=12)
    all_header_row = all_title_row + 1
    write_dataframe(ws, all_df, all_header_row)
    style_header_row(ws, all_header_row, 1, len(columns))

    ws.freeze_panes = None
    ws.auto_filter.ref = f"A{all_header_row}:{get_column_letter(len(columns))}{all_header_row + len(all_df)}"

    numeric_prefixes = ("ratio", "std.error", "Jackknife")
    for row in ws.iter_rows(min_row=passed_header_row + 1):
        for cell in row:
            header = ws.cell(passed_header_row if cell.row < all_header_row else all_header_row, cell.column).value
            if header == "tail" or (isinstance(header, str) and header.startswith(numeric_prefixes)):
                cell.number_format = "0.0000" if header in {"tail"} or str(header).startswith("Jackknife") else "0.000"

    header_rows = {1, passed_header_row, all_header_row}
    title_rows = {passed_title_row, all_title_row}
    for row in ws.iter_rows():
        for cell in row:
            if cell.row in header_rows:
                cell.font = font_for_value(cell.value, bold=True)
                cell.fill = PatternFill(fill_type="solid", fgColor="FFFFFF")
            elif cell.row in title_rows:
                cell.font = font_for_value(cell.value, bold=True, size=12)
            elif cell.value is not None and not (cell.column == 2 and isinstance(cell.value, str) and cell.value.startswith("#")):
                cell.font = font_for_value(cell.value)

    for col_idx in range(1, ws.max_column + 1):
        letter = get_column_letter(col_idx)
        max_len = 0
        for cell in ws[letter][:300]:
            if cell.value is not None:
                max_len = max(max_len, len(str(cell.value)))
        ws.column_dimensions[letter].width = min(max(max_len + 2, 10), 45)
    ws.column_dimensions["A"].width = max(ws.column_dimensions["A"].width, 24)
    ws.column_dimensions["B"].width = max(ws.column_dimensions["B"].width, 26)
    ws.column_dimensions["C"].width = max(ws.column_dimensions["C"].width, 24)

    wb.save(out_path)


def main() -> None:
    parser = argparse.ArgumentParser(description="Collect qpAdm result.txt files into Excel.")
    parser.add_argument("--root", default="output", help="Root result directory generated by qpadm_v4.sh.")
    parser.add_argument("--out", default="qpadm_summary.xlsx", help="Output .xlsx path.")
    parser.add_argument("--tail", type=float, default=0.05, help="Tail threshold, e.g. 0.01, 0.03, 0.05.")
    parser.add_argument("--strategy", choices=["rotating", "non-rotating"], help="Strategy used in qpadm_v4.sh. Auto-inferred when possible.")
    parser.add_argument("--target-file", help="Optional target population list. Defaults to ./target.txt if present.")
    parser.add_argument("--source-file", help="Optional source file with ==== tags. Defaults to ./source.txt if present.")
    parser.add_argument("--right-file", help="Optional fixed outgroup list. Defaults to ./right.txt if present.")
    args = parser.parse_args()

    root = Path(args.root).resolve()
    result_files = sorted(root.rglob("result.txt"))
    if not result_files:
        raise SystemExit(f"No result.txt found under: {root}")

    rows = [parse_result(path, args.tail) for path in result_files]
    target_file = Path(args.target_file) if args.target_file else Path("target.txt")
    if not target_file.exists():
        target_file = None
    source_file = Path(args.source_file) if args.source_file else Path("source.txt")
    if not source_file.exists():
        source_file = None
    right_file = Path(args.right_file) if args.right_file else Path("right.txt")
    if not right_file.exists():
        right_file = None

    targets = read_lines(target_file) if target_file else sorted({r["target"] for r in rows if r.get("target")})
    sources = parse_source_file(source_file) if source_file else sorted({s for r in rows for s in str(r.get("sources", "")).split(";") if s})
    source_groups = parse_source_groups(source_file) if source_file else []
    strategy = args.strategy or infer_strategy(rows, sources)
    if right_file:
        outgroups = read_lines(right_file)
    else:
        source_set = set(sources)
        outgroups = sorted(
            {
                g
                for r in rows
                for g in str(r.get("outgroups", "")).split(";")
                if g and g not in source_set
            }
        )
    for row in rows:
        row["strategy"] = strategy or ""

    out_path = Path(args.out).resolve()
    write_excel(rows, out_path, targets, sources, source_groups, outgroups, strategy, args.tail)

    passed = sum(1 for r in rows if r["pass"])
    print(f"Found result.txt: {len(rows)}")
    print(f"Passed models:    {passed}")
    print(f"Failed models:    {len(rows) - passed}")
    print(f"Excel written:     {out_path}")


if __name__ == "__main__":
    main()
